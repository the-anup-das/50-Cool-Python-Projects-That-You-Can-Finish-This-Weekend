def calculator():
    FLAG_END = False

    num1 = float(input("What's the first number?: "))

    while not FLAG_END:
        num2 = float(input("What's the second number?: "))

        for operation in operations:
            print(operation)

        symbol = input("Pick an operation from above: ")
        function = operations[symbol]
        answer = function(num1,num2)

        print(f"{num1} {symbol} {num2} = {answer}")
        num1 = answer

        #Continue the program or not
        user_response = input(f"\nType 'y' to continue calculating with '{num1}',\nor type 'x' to exit \ntype 'n' to start new calculation: ").lower()
        if user_response == 'x':
            FLAG_END = True
            print("Bye!")
        elif user_response == 'n':
            calculator()

